STACK SEGMENT USE16 STACK
DB 200 DUP(0)
STACK ENDS
DATA SEGMENT USE16
    INPUT DB "INPUT: $"
    OUTPUT DB "OUTPUT: $"
    BUF DB "123456789abcdef"
    BUF2 DB 11 
         DB ?
         DB 11 DUP(0)
    CRLF DB 0DH,0AH,'$'
DATA ENDS
CODE SEGEMENT USE16
ASSUME CS:CODE,DS:DATA,SS:STACK
L_INPUT:
    LEA DX,STR
    MOV AH 9
    INT 21H

    MOV AH,1
    INT 21H

    CMP AL,0
    JE EXIT

    LEA DX,IS
    MOV AH 9
    INT 21H
    JMP SHOW

SHOW:
    MOV AH,0
    MOV BX,16
LOOP_SHOW:
    MOV DX,0
    DIV BX;
    PUSH DX;
    CMP AX,0;
    JNE LOOP_SHOW;
    MOV CX,2
LOOPA:
    POP SI;
    MOVZX DX,BUF[SI];
    MOV AH,2
    INT 21H
    LOOP LOOPA
    LEA DX,CRLF
    MOV AH,9
    INT 21H
    JMP L_INPUT
EXIT: